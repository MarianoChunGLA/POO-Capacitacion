public class Recibo {

    private int totalElementosDepositados;
    private double valorTotal;

    public int getTotalElementosDepositados() {
        return totalElementosDepositados;
    }

    public double getValorTotal() {
        return valorTotal;
    }

    public void setTotalElementosDepositados(int totalElementosDepositados) {
        this.totalElementosDepositados = totalElementosDepositados;
    }

    public void setValorTotal(double valorTotal) {
        this.valorTotal = valorTotal;
    }
}
