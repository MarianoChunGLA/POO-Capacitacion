public class Lata {
    private String tipoElemento;
    private double valorDevolucion;

    public Lata() {
        this.tipoElemento = "Lata";
        this.valorDevolucion = 20.0;
    }

    public String getTipoElemento() {
        return tipoElemento;
    }

    public double getValorDevolucion() {
        return valorDevolucion;
    }
}
